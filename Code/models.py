# This file contains the models for EpiCastBench codes

from darts.models import (
    NBEATSModel, NHiTSModel, TCNModel, RNNModel, NaiveMean,
    BlockRNNModel, TransformerModel, DLinearModel, TiDEModel,
    TSMixerModel, Chronos2Model, TimesFM2p5Model, XGBModel, RandomForestModel, NeuralForecastModel
)


def get_models(inp, out, epochs, seed):
    return {
        "Naive": NaiveMean(),
        "DLinear": DLinearModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed),
        "RandomForest": RandomForestModel(lags=inp, output_chunk_length=out, random_state=seed),
        "XGBoost": XGBModel(lags=inp, output_chunk_length=out, random_state=seed),
        "TSMixer": TSMixerModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed),
        "KAN": NeuralForecastModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, model="KAN", random_state=seed),
        "LSTM": BlockRNNModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed, model="LSTM"),
        "DeepAR": RNNModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed, model="LSTM", training_length=30),
        "TCN": TCNModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed),
        "NBEATS": NBEATSModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed),
        "NHITS": NHiTSModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed),
        "Transformer": TransformerModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed),
        "TiDE": TiDEModel(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed),
        "Chronos2": Chronos2Model(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed),
        "TimesFM": TimesFM2p5Model(input_chunk_length=inp, output_chunk_length=out, n_epochs=epochs, random_state=seed)
    }